package routers

import (
	"do-mall/controller/api/admin/AdminController"
	OrderController2 "do-mall/controller/api/admin/OrderController"
	ProductController2 "do-mall/controller/api/admin/ProductController"
	"do-mall/controller/api/v1/AddressController"
	"do-mall/controller/api/v1/CartController"
	"do-mall/controller/api/v1/OrderController"
	"do-mall/controller/api/v1/ProductController"
	"do-mall/controller/api/v1/UserController"
	v2UserController "do-mall/controller/api/v2/UserController"
	_ "do-mall/docs" // docs is generated by Swag CLI, you have to import it.
	jwt "do-mall/middleware"
	"do-mall/pkg/e"
	"do-mall/pkg/setting"
	"github.com/gin-contrib/cors"
	"github.com/gin-gonic/gin"
	ginSwagger "github.com/swaggo/gin-swagger"
	"github.com/swaggo/gin-swagger/swaggerFiles"
)

func InitRouter() *gin.Engine {
	r := gin.New()
	r.Use(gin.Logger())
	r.Use(gin.Recovery())
	r.Use(cors.Default())

	gin.SetMode(setting.RunMode)

	// use ginSwagger middleware to
	r.GET("/swagger/*any", ginSwagger.WrapHandler(swaggerFiles.Handler))

	// 注册路由
	apiv1 := r.Group("/api/v1")
	{
		// User
		userRoute := apiv1.Group("/user")
		{
			userRoute.POST("/register", UserController.Create)
			userRoute.POST("/login", UserController.Login)
			userRoute.GET("/index", jwt.JWT(), UserController.Show)
			userRoute.PUT("/edit", jwt.JWT(), UserController.Update)

			// Favorites
			userRoute.GET("/favorite", jwt.JWT(), UserController.FavoritesList)
			userRoute.POST("/favorite", jwt.JWT(), UserController.FavoritesCreate)
			userRoute.DELETE("/favorite/product/:id", jwt.JWT(), UserController.FavoritesDestroy)

			// Shopping Cart
			userRoute.GET("/shoppingCart", jwt.JWT(), CartController.Index)
			userRoute.POST("/shoppingCart", jwt.JWT(), CartController.Create)
			userRoute.DELETE("/shoppingCart/:id", jwt.JWT(), CartController.Destroy)
			userRoute.DELETE("/shoppingCart/:id/number", jwt.JWT(), CartController.Decrease)

			// Addresses
			userRoute.GET("/address", jwt.JWT(), AddressController.Index)
			userRoute.POST("/address", jwt.JWT(), AddressController.Create)
			userRoute.DELETE("/address/:id", jwt.JWT(), AddressController.Destroy)
			userRoute.PUT("/address/:id/default", jwt.JWT(), AddressController.SetDefault)
			userRoute.PUT("/address/:id", jwt.JWT(), AddressController.Update)

			// Order
			userRoute.GET("/order", jwt.JWT(), OrderController.List)
			userRoute.POST("/order", jwt.JWT(), OrderController.Create)
			userRoute.DELETE("/order/:id", jwt.JWT(), OrderController.Destroy)
			userRoute.POST("/order/:id/pay", jwt.JWT(), OrderController.Pay)

			// Wallet
			userRoute.POST("/balance", jwt.JWT(), UserController.TopUp)

		}

		// Products
		productRoute := apiv1.Group("/product")
		{
			productRoute.GET("/index", ProductController.Index)
			productRoute.GET("/search", ProductController.Search)
			productRoute.GET("/info/:id", ProductController.Show)
			// Inventory
			productRoute.GET("/inventory/:pid", ProductController.GetInventory)

		}

	}

	// V2 微信接口
	apiv2 := r.Group("/api/v2")
	{
		v2wxUser := apiv2.Group("/wx/user")
		{
			v2wxUser.GET("/", v2UserController.WxLogin)
			v2wxUser.POST("/", jwt.JWT(), v2UserController.WxGetUserInfo)
			v2wxUser.POST("/phone", jwt.JWT(), v2UserController.WxGetPhone)
		}

		v2wxPay := apiv2.Group("/pay")
		{
			v2wxPay.POST("/topup", jwt.JWT(), v2UserController.WxTopup)
			v2wxPay.Any("/topup/callback", v2UserController.WxTopup)
		}
	}

	// 注册后台路由
	r.POST("/adminUserLogin", AdminController.Login)
	admin := r.Group("/admin")
	admin.Use(jwt.Admin())
	{
		// Admin Info
		admin.GET("/info", AdminController.Show)

		// Products
		adminProductRoute := admin.Group("/product")
		{
			adminProductRoute.POST("/", ProductController2.Create)
			adminProductRoute.PUT("/:oid", ProductController2.Update)
			adminProductRoute.DELETE("/:oid", ProductController2.Destroy)
			// Inventory
			adminProductRoute.PUT("/:oid/inventory", ProductController2.UpdateInventory)
		}
		adminOrderRoute := admin.Group("/order")
		{
			adminOrderRoute.POST("/:oid/express", OrderController2.Update)
		}

	}

	r.NoRoute(func(c *gin.Context) {
		c.JSON(404, gin.H{
			"code": 404,
			"msg":  e.GetMsg(404),
			"data": nil,
		})
	})

	return r
}

